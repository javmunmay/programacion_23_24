public class ejercicio1 {
    public static void main(String[] args) {

        // El tipo short permite almacenar valores comprendidos entre -32768 y 32767.
        // Realiza un programa que muestre si el rango de valores de un tipo se comporta de
        // forma cíclica. Es decir, que muestre el valor máximo de una variable de tipo short,
        // y muestre también qué ocurre si sumamos 1 a esa variable.

        short valorMaximo = 32767;

        System.out.println("Valor máximo de una variable de tipo short: "+valorMaximo);

        valorMaximo++;

        System.out.println("Valor máximo más 1 es igual a: "+valorMaximo);






    }
}
